----------------------------Convertir a campo calculado sldtotal
CREATE OR REPLACE FUNCTION calc_func_sldtotal() RETURNS TRIGGER AS
$BODY$
BEGIN
	IF(TG_OP = 'UPDATE') THEN
		UPDATE saldo SET sldsaldo = OLD.sldsaldo - NEW.sldreserva;
	END IF;
	NEW.sldtotal := NEW.sldsaldo - NEW.sldreserva;
	RETURN NEW;
END;
$BODY$ LANGUAGE plpgsql

CREATE TRIGGER calc_trg_sldtotal
BEFORE INSERT OR UPDATE ON saldo FOR EACH ROW EXECUTE PROCEDURE calc_func_sldtotal();

