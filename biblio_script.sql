-----Copias
CREATE OR REPLACE FUNCTION addCopias(t_proId copia.proId%TYPE,t_cpUbicacion copia.cpUbicacion%TYPE,t_numCopias INTEGER,t_cfgId copia.cfgId%TYPE)
RETURNS TEXT AS $BODY$
DECLARE
	codigo CHARACTER VARYING(250);
BEGIN
	FOR i IN 1..t_numCopias LOOP
		INSERT INTO Copia(proId,cpUbicacion,cfgId) VALUES(t_proId,t_cpUbicacion,t_cfgId);
		IF (i > 1) THEN
			codigo := codigo || ', ' || (SELECT last_value FROM sq_cpId);
		ELSE
			codigo := (SELECT last_value FROM sq_cpId);
		END IF;
	END LOOP;
	RETURN (SELECT codigo::CHARACTER VARYING AS codigo);
END;
$BODY$ LANGUAGE plpgsql;



-----Prestamo
CREATE OR REPLACE FUNCTION prestamo(t_prstid prestamo.prstid%TYPE,t_prstfechapres prestamo.prstfechapres%TYPE,t_prstfechaprevdev prestamo.prstfechaprevdev%TYPE,t_prstfechadev prestamo.prstfechadev%TYPE,
t_usrid prestamo.usrid%TYPE,t_lbrid prestamo.lbrid%TYPE,t_prstdev prestamo.prstdev%TYPE,t_cfgid prestamo.cfgid%TYPE,operacion CHARACTER VARYING(10)) RETURNS VOID AS $BODY$
BEGIN
	CASE
		WHEN(operacion = "INSERT") THEN
			IF((SELECT sldsaldo FROM saldo WHERE lbrid = t_lbrid) > 0) THEN --Verifica disponibilidad del libro
				IF((SELECT COUNT(prstid) FROM prestamo WHERE usrid = t_usrid AND prsetdev = 'F') < (SELECT cfgmaxprestamos FROM configuracion)) THEN
				--Verifica si no ha excedido el límite de prestamos
					IF((SELECT SUM(prstmora) AS mora FROM prestamo WHERE usrid = t_usrid AND prsetdev = 'F') > 0.0) THEN
						RAISE EXCEPTION 'El usuario tiene mora pendiente';
					ELSE
						INSERT INTO prestamo(prstfechaprevdev,usrid,lbrid,cfgid) VALUES(now() + interval '1 day',t_usrid,t_lbrid,t_cfgid);
					END IF;
				ELSE
					RAISE EXCEPTION 'El usuario ha excedido el límite de prestamos';
				END IF;
			ELSE
				RAISE EXCEPTION 'El libro no está disponible en este lugar';
			END IF;
		WHEN(operacion = "RENADM") THEN --renovacion encargard/a de biblioteca
			UPDATE prestamo SET prstfechaprevdev = t_prstfechaprevdev WHERE prstid = t_prstid;
		WHEN(operacion = "PAGO") THEN --pago de mora
			UPDATE prestamo SET prsmora = 0.0 WHERE prstid = t_prstid;
		WHEN(operacion = "RENUSR") THEN --renovación por el usuario
			UPDATE prestamo SET prstfechaprevdev = (now() + interval '1 day') WHERE prstid = t_prstid;
		ELSE --devolucion
			UPDATE prestamo SET prstfechadev = now(), prstdev = 'T' WHERE prstid = t_prstid;
			UPDATE saldos SET sldsaldo = sldsaldo + 1 WHERE lbrid = t_lbrid AND cfgid = t_cfgid;
	END CASE;
END;
$BODY$ LANGUAGE plpgsql;

