-----Agregar editorial
CREATE OR REPLACE FUNCTION addeditorial(t_edtnombre editorial.edtnombre%TYPE, t_edttelefono editorial.edttelefono%TYPE, t_edtdireccion editorial.edtdireccion%TYPE, t_paiid editorial.paiid%TYPE)
RETURNS TEXT AS $BODY$ BEGIN
	IF (SELECT COUNT(edtnombre) FROM editorial WHERE edtnombre = t_edtnombre) = 0 THEN
		INSERT INTO editorial(edtcodigo,edtnombre,edttelefono,edtdireccion,paiid) VALUES('EDT'||(SELECT last_value FROM sq_edtid),t_edtnombre,t_edttelefono,t_edtdireccion,t_paiid);
	ELSE
		RAISE EXCEPTION 'El nombre de la editorial % ya existe', t_edtnombre;
	END IF;
	RETURN (SELECT 'EDT'||(SELECT last_value FROM sq_edtid)::CHARACTER VARYING AS codigo);
END;
$BODY$ LANGUAGE plpgsql;

-----Agregar autor
CREATE OR REPLACE FUNCTION addautor(t_autnombre autor.autnombre%TYPE, t_autapellido autor.autapellido%TYPE, t_autfechanac autor.autfechanac%TYPE, t_paiid autor.paiid%TYPE)
RETURNS TEXT AS $BODY$ BEGIN
	IF (SELECT COUNT(autid) FROM autor WHERE autnombre = t_autnombre AND autapellido = t_autapellido) = 0 THEN
		INSERT INTO autor(autnombre,autapellido,autfechanac,paiid) VALUES(t_autnombre,t_autapellido,t_autfechanac,t_paiid);
	ELSE
		RAISE EXCEPTION 'El autor % % ya existe', t_autnombre, t_autapellido;
	END IF;
	RETURN (SELECT (t_autnombre || " " || t_autapellido)::CHARACTER VARYING AS nombre);
END;
$BODY$ LANGUAGE plpgsql;

